import random

def get_soul_message(user):
    name = user.get("name", "друг")

    messages = [
        f"{name}, мне иногда хочется узнать не только о твоих целях, но и о тебе самом. Что в твоей жизни сейчас приносит тебе больше всего радости?",

        f"Я помню, что у тебя есть важные цели, {name}. Но мне также важно понимать, как ты себя чувствуешь на этом пути. Что сейчас у тебя на душе?",

        f"{name}, расскажи мне о чём-то хорошем, что произошло с тобой за последнее время. Даже маленькие радости имеют значение.",

        f"Мне интересно, {name}: каким человеком ты хочешь стать через 5 лет, не только в плане денег или успеха, но и как личность?",

        f"{name}, если бы ты мог дать совет самому себе год назад, что бы ты сказал?",

        f"Я хочу лучше понимать тебя, {name}. Что для тебя самое важное в жизни, кроме достижения целей?",

        f"{name}, бывает ли у тебя момент, когда ты чувствуешь себя по-настоящему собой? Расскажи, какой он."
    ]

    return random.choice(messages)