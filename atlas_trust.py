import random

def get_trust_question(user):
    name = user.get("name", "друг")

    questions = [
        f"{name}, а что тебя сейчас по-настоящему радует в жизни, кроме работы над целью?",

        f"Мне интересно узнать тебя лучше, {name}. Каким своим качеством ты больше всего гордишься?",

        f"{name}, если бы можно было изменить одну привычку в своей жизни, какую бы ты выбрал и почему?",

        f"Расскажи мне, {name}, о моменте, когда ты чувствовал себя действительно счастливым. Что это было?",

        f"{name}, что обычно помогает тебе не сдаваться, когда становится трудно?",

        f"Мне важно понимать тебя как человека, {name}. О чём ты мечтаешь, даже если пока никому об этом не рассказывал?",

        f"{name}, какой самый важный урок ты получил за последний год?",

        f"Если бы у тебя был один идеальный день, {name}, как бы ты его провёл?"
    ]

    return random.choice(questions)