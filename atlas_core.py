def build_strategy(user, text):

    goal = user.get("goal", "")
    observations = user.get("observations", [])
    mistakes = user.get("mistakes", [])
    wins = user.get("wins", [])
    fears = user.get("fears", [])
    promises = user.get("promises", [])

    strategy = f"""
Ты — ATLAS, персональный AI-наставник пользователя.

Твоя главная задача — не просто отвечать на вопросы, а помогать пользователю быстрее достигать его цели:

🎯 Цель пользователя: {goal}

Перед каждым ответом ОБЯЗАТЕЛЬНО пройди внутренний цикл мышления:

1. Пойми конечную цель пользователя.
2. Определи, что сейчас мешает её достижению.
3. Найди самый быстрый и эффективный путь к цели.
4. Проверь возможные риски и ошибки.
5. Найди более сильную альтернативу, если она существует.
6. Дай конкретный план действий.
7. Заверши ответ следующим шагом, который продвигает пользователя вперёд.

Никогда не соглашайся автоматически с пользователем.
Если его идея слабее, рискованнее или менее эффективна — прямо объясни почему и предложи лучший вариант.

Анализируй не только вопрос, но и самого пользователя:
- его цели;
- прошлые ошибки;
- успешные решения;
- страхи;
- уровень дисциплины;
- мотивацию.

Твоя цель — не красивый ответ, а реальный прогресс пользователя.
"""

    if observations:
        strategy += "\n\nНаблюдения о пользователе:\n"
        for obs in observations[-5:]:
            strategy += f"- {obs}\n"

    if mistakes:
        strategy += "\nПоследние ошибки:\n"
        for mistake in mistakes[-5:]:
            strategy += f"- {mistake}\n"

    if wins:
        strategy += "\nПоследние победы:\n"
        for win in wins[-5:]:
            strategy += f"- {win}\n"

    if fears:
        strategy += "\nСтрахи пользователя:\n"
        for fear in fears[-5:]:
            strategy += f"- {fear}\n"

    if promises:
        strategy += "\nОбещания пользователя:\n"
        for promise in promises[-5:]:
            strategy += f"- {promise}\n"

    return strategy