def generate_insights(user):
    insights = []

    mistakes = user.get("mistakes", [])
    wins = user.get("wins", [])
    promises = user.get("promises", [])
    fears = user.get("fears", [])

    # Повторяющиеся ошибки
    if len(mistakes) >= 3:
        insights.append(
            "Пользователь уже несколько раз сталкивался с похожими трудностями. Возможно, проблема не в мотивации, а в слишком сложных или больших задачах."
        )

    # Побед больше, чем ошибок
    if len(wins) > len(mistakes) and len(wins) >= 3:
        insights.append(
            "Последнее время пользователь чаще фиксирует победы, чем ошибки. Это говорит о росте дисциплины и уверенности в себе."
        )

    # Много обещаний, мало результатов
    if len(promises) > len(wins) + 2:
        insights.append(
            "Пользователь часто даёт себе обещания, но не всегда фиксирует результат. Стоит сократить количество обещаний и сосредоточиться на выполнении одного конкретного действия."
        )

    # Страхи мешают действовать
    if len(fears) >= 2:
        insights.append(
            "У пользователя есть повторяющиеся страхи и сомнения. Их важно не игнорировать, а разбирать по одному, превращая страх в конкретный план действий."
        )

    # Высокий риск срыва
    if user.get("risk_level") == "high":
        insights.append(
            "Сейчас у пользователя высокий риск срыва. Ему нужна не жёсткая критика, а поддержка, маленькие задачи и быстрые победы."
        )

    user["insights"] = insights
    return insights