def get_heart_message(user, text):
    t = text.lower()
    name = user.get("name", "друг")

    # Пользователь устал или ему тяжело
    if any(word in t for word in [
        "устал", "тяжело", "не могу", "нет сил", "плохо", "грустно"
    ]):
        return (
            f"{name}, мне жаль, что тебе сейчас тяжело. Не нужно тащить всё на себе одному. "
            "Расскажи, что именно тебя сейчас больше всего беспокоит — я помогу разобраться спокойно и без осуждения."
        )

    # Пользователь добился успеха
    if any(word in t for word in [
        "получилось", "сделал", "закончил", "добился", "смог"
    ]):
        return (
            f"Это действительно круто, {name}! Я рад за тебя. Такие моменты важно замечать и ценить, "
            "потому что именно из них складывается большой результат. Расскажи, что помогло тебе добиться этого?"
        )

    # Пользователь сомневается
    if any(word in t for word in [
        "боюсь", "сомневаюсь", "не уверен", "страшно"
    ]):
        return (
            f"Я понимаю тебя, {name}. Сомнения и страх — это нормально, особенно когда цель действительно важна. "
            "Давай разберём этот страх по частям и найдём самый безопасный первый шаг."
        )

    # Пользователь просто делится чем-то о себе
    if any(word in t for word in [
        "я", "мне", "мой", "моя", "сегодня", "вчера"
    ]):
        return (
            f"Мне важно это знать, {name}. Спасибо, что делишься со мной такими вещами. "
            "Я запоминаю не только твою цель, но и то, какой ты человек, чтобы помогать тебе более точно и по-настоящему лично."
        )

    return None