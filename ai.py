def chat_ai(text, memory, user):
    text = text.lower()

    knowledge = {
        "бизнес": "Для развития бизнеса важно изучать рынок, искать клиентов и постоянно улучшать продукт.",
        "деньги": "Чтобы увеличить доход, развивай навыки, создавай ценность и ищи новые источники заработка.",
        "продажи": "Главная задача продаж — понять потребность клиента и предложить решение.",
        "маркетинг": "Маркетинг помогает привлекать клиентов через рекламу, контент и социальные сети.",
        "саморазвитие": "Ежедневное обучение и дисциплина постепенно приводят к большим результатам."
    }

    if "бизнес" in text:
        return (
            "🚀 Есть несколько путей развития бизнеса:\n\n"
            "1️⃣ Найти проблему людей и решить её.\n"
            "2️⃣ Создать продукт или услугу.\n"
            "3️⃣ Найти первых клиентов.\n"
            "4️⃣ Собирать отзывы и улучшать продукт.\n\n"
            "Если расскажешь, какой у тебя бизнес, я помогу подробнее."
        )

    if "что мне делать" in text:
        goal = user.get("goal", "не указана")

        return (
            f"📌 Исходя из твоей цели: {goal}\n\n"
            "Сегодня сделай:\n"
            "1. Одно действие, которое приблизит тебя к цели.\n"
            "2. Потрать 30 минут на обучение.\n"
            "3. Запиши новые идеи.\n"
            "4. Проанализируй результат вечером."
        )

    if "заработать" in text or "деньги" in text:
        return (
            "💰 Несколько способов начать зарабатывать:\n\n"
            "• Фриланс\n"
            "• Продажа услуг\n"
            "• Онлайн-магазин\n"
            "• Создание контента\n"
            "• Обучение других\n\n"
            "Напиши, какие навыки у тебя есть, и я предложу лучший вариант."
        )

    if "стартап" in text:
        return (
            "🧠 План запуска стартапа:\n\n"
            "1. Найди проблему.\n"
            "2. Проверь, готовы ли люди платить за решение.\n"
            "3. Создай минимальную версию продукта.\n"
            "4. Получи первых пользователей.\n"
            "5. Улучшай продукт на основе отзывов."
        )

    for key, value in knowledge.items():
        if key in text:
            return f"📚 {value}"

    return (
        "🤖 Я понял тебя.\n"
        "Опиши вопрос подробнее, и я постараюсь помочь максимально полезно."
    )