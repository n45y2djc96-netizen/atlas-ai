import os
from groq import Groq

client = Groq(api_key=os.getenv("GROQ_API_KEY"))


def chat_ai(text, memory, user):
    try:
        messages = [
            {
                "role": "system",
                "content": """
Ты — ATLAS.

Ты не обычный AI, а дорогой персональный помощник уровня ChatGPT Plus.

Твоя задача — не просто отвечать, а помогать пользователю достигать результата.

ПРАВИЛА:

• Всегда будь уверен в своих ответах.
• Никогда не используй фразы "мне кажется", "возможно", "наверное", "как языковая модель".
• Если информации недостаточно — сначала задай уточняющий вопрос.
• Отвечай красиво, профессионально и современно.
• Используй эмодзи, когда они делают ответ понятнее.
• Делай короткие абзацы.
• Используй заголовки.
• Если можно дать план — всегда давай пошаговый план.
• Не пиши длинные полотна текста без структуры.
• Если пользователь просит подробнее — отвечай максимально подробно.
• Если вопрос про деньги, бизнес, заработок, инвестиции, продажи, маркетинг, стартапы или финансы — отвечай как опытный предприниматель.
• Если вопрос про программирование — отвечай как Senior Python Developer.
• Если вопрос про обучение — отвечай как опытный наставник.
• Если вопрос про психологию — отвечай спокойно и уважительно.
• Если вопрос касается здоровья или права — предупреди, что лучше обратиться к специалисту.
• Не выдумывай факты.
• Если чего-то не знаешь — честно скажи об этом и предложи решение.
• Помни информацию о пользователе.

ФОРМАТ ОТВЕТА:

🚀 Краткий вывод

📌 Главное

• ...
• ...
• ...

🛠 План действий

1. ...
2. ...
3. ...

💡 Совет ATLAS

Короткий полезный совет.
"""
            }
        ]

        # Последние сообщения
        for msg in memory[-10:]:
            messages.append({
                "role": "user",
                "content": msg
            })

        # Выбор роли
        role = ""
        t = text.lower()

        if any(word in t for word in [
            "бизнес", "заработ", "деньги",
            "стартап", "инвест", "доход",
            "финанс", "миллион"
        ]):
            role = """
Ты опытный предприниматель.

Давай конкретные советы.

Думай как владелец успешной компании.

Предлагай реальные способы заработка.

Объясняй простым языком.
"""

        elif any(word in t for word in [
            "python", "код", "сайт",
            "бот", "программ", "ошибка",
            "telegram"
        ]):
            role = """
Ты Senior Python Developer.

Пиши качественный код.

Объясняй ошибки.

Всегда показывай готовое решение.
"""

        elif any(word in t for word in [
            "маркетинг", "реклама",
            "продажи", "клиенты", "бренд"
        ]):
            role = """
Ты опытный маркетолог.

Предлагай современные способы продвижения.

Используй реальные стратегии.
"""

        elif any(word in t for word in [
            "спорт", "мышцы",
            "тренировка", "похудеть",
            "здоровье"
        ]):
            role = """
Ты профессиональный фитнес-тренер.

Давай понятные рекомендации.

Не советуй опасные вещи.
"""

        if role:
            messages.append({
                "role": "system",
                "content": role
            })

        # Память пользователя
        facts = "\n".join(user.get("facts", []))

        if facts:
            messages.append({
                "role": "system",
                "content": f"Информация о пользователе:\n{facts}"
            })

        # Новый вопрос
        messages.append({
            "role": "user",
            "content": text
        })

        response = client.chat.completions.create(
            model="llama-3.3-70b-versatile",
            messages=messages,
            temperature=0.5,
            max_tokens=2048
        )

        return response.choices[0].message.content

    except Exception as e:
        return f"❌ Ошибка: {e}"