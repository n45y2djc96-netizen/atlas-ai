import random
from atlas_trust import get_trust_question
from atlas_soul import get_soul_message

async def send_coach_message(bot, user_id, user):
    name = user.get("name", "друг")
    goal = user.get("goal", "своей цели")
    risk = user.get("risk_level", "low")

    # Сообщения при высоком риске срыва
    if risk == "high":
        messages = [
            f"💙 {name}, я вижу, что тебе сейчас непросто. Но один плохой период не определяет твоё будущее. Давай сегодня сделаем хотя бы один маленький шаг к цели: {goal}.",

            f"🎯 {name}, не позволяй нескольким неудачам убедить тебя, что ты не способен на большее. Ты уже начал путь к цели: {goal}. Продолжай, даже если шаг будет маленьким.",

            f"🚀 {name}, сейчас важно не сделать всё идеально, а просто не остановиться. Напиши мне, что именно мешает тебе двигаться к цели: {goal}. Мы найдём решение вместе."
        ]

    # Обычные ежедневные сообщения
    else:
        messages = [
            f"👋 {name}, как проходит твой день? Что ты уже сделал сегодня для цели: {goal}?",

            f"🎯 {name}, помни: твоя цель — {goal}. Какой один шаг ты можешь сделать прямо сейчас?",

            f"🚀 {name}, не жди идеального момента. Маленькое действие сегодня лучше, чем большой план завтра.",

            f"💪 {name}, я рядом, чтобы помочь тебе не сойти с пути. Что сейчас мешает двигаться к цели?",

            f"📈 {name}, каждый день без действия отдаляет тебя от цели. Давай сегодня сделаем хотя бы один шаг."
        ]

    # Выбираем основное сообщение
    message = random.choice(messages)

    # Иногда добавляем вопрос доверия, а не заменяем сообщение
    if random.random() < 0.4:  # 40% сообщений
        message += "\n\n" + get_trust_question(user)

    # Редко начинаем более глубокий разговор
    if random.random() < 0.2:  # 20% сообщений
        message += "\n\n" + get_soul_message(user)
    
    await bot.send_message(
        chat_id=user_id,
        text=message
    )